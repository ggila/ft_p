#include "libft.h"
#include <stdlib.h>

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	l1;
	size_t	l2;
	char	*str;

	if (!s1 && !s2)
		return (NULL);
	if (!s1)
		return (ft_strdup(s2));
	if (!s2)
		return (ft_strdup(s1));
	l1 = ft_strlen(s1);
	l2 = ft_strlen(s2);
	if (!(str = (char*)malloc(sizeof(char) * (l1 + l2 + 1))))
		return (NULL);
	ft_strncpy(str, s1, l1);
	ft_strcpy(str + l1, s2);
	return (str);
}
